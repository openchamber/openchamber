import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const packageRoot = path.resolve(__dirname, '..')
const messagesDir = path.join(packageRoot, 'messages')
const sourceLocalePath = path.join(messagesDir, 'en.json')
const outputPath = path.join(packageRoot, 'src/lib/i18n/messages.ts')

const toCamelCase = (key) => key.replace(/_([a-zA-Z0-9])/g, (_, char) => char.toUpperCase())

const source = JSON.parse(fs.readFileSync(sourceLocalePath, 'utf8'))
const keys = Object.keys(source).sort()

const header = `/**
 * Auto-generated Paraglide compatibility layer.
 *
 * Source of truth: packages/ui/messages/*.json
 * Generated runtime: packages/ui/src/paraglide/*
 *
 * This file preserves the existing camelCase message API while delegating to
 * official Paraglide generated snake_case exports.
 */

// @ts-expect-error generated by Paraglide at build time
import * as generated from '../../paraglide/messages.js'

type GeneratedMessageFn = (...args: unknown[]) => string

const placeholderInputs = new Proxy<Record<string, string>>(
  {},
  {
    get: (_target, prop) => \`{\${String(prop)}}\`,
  },
)

function compatMessage<T extends GeneratedMessageFn>(fn: T) {
  return ((inputs?: Parameters<T>[0], options?: Parameters<T>[1]) => {
    if (inputs === undefined && fn.length > 0) {
      return fn(placeholderInputs as Parameters<T>[0], options)
    }

    return fn(inputs as Parameters<T>[0], options)
  }) as (inputs?: Parameters<T>[0], options?: Parameters<T>[1]) => ReturnType<T>
}

`

const namedExports = keys
  .map((key) => `export const ${toCamelCase(key)} = compatMessage(generated.${key})`)
  .join('\n')

const namespaceExports = keys
  .map((key) => {
    const camel = toCamelCase(key)
    return `  ${camel},`
  })
  .join('\n')

const content = `${header}${namedExports}

export const m = {
${namespaceExports}
}
`

fs.mkdirSync(path.dirname(outputPath), { recursive: true })
fs.writeFileSync(outputPath, content)

console.log(`Generated compatibility messages wrapper at ${outputPath}`)
